num1=int(input("enter the first_number"))
num2=int(input("enter the second_number"))
symbol=(input("enter your symbol"))
if symbol=="+":
    print(num1+num2)
elif symbol=="-":
    print(num1-num2)
elif symbol=="*":
    print(num1*num2)   
elif symbol=="/":
    print(num1/num2)
elif symbol=="//":
    print(num1//num2)  
elif symbol=="**":
    print(num1**num2)  
else:
    print("check your value")


    






